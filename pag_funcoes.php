<?php 

    function calcula($a, $b) 
    {
        $resultado = $a + $b;
        return $resultado;
    }

    function quadrado($a) 
    {
        $resultado = $a * $a;
        return $resultado;
    }

    function comprimento($palavra)
    {
        $resultado = strlen($palavra);
        return $resultado;
    }

